meu_set = {4,5,2,3,1,2,3,4,5} # Set (conjunto) utiliza chaves {} e remove valores duplicados.
print(f"Set: {meu_set}") 

minha_lista = [4,5,2,3,1,2,3,4,5] # List (lista) utiliza colchetes [] e mantém valores duplicados. É mutável.
print(f"Lista: {minha_lista}")

minha_tupla = (4,5,2,3,1,2,3,4,5) # Tuple (tupla) utiliza parênteses () e mantém valores duplicados. É imutável.
print(f"Tupla: {minha_tupla}")

meu_dicionario = {'a':1, 'b':'jean', 'c':3, 'd':4, 'e':5} # Dictionary (dicionário) utiliza chaves {} e trabalha com pares chave:valor.
print(f"Dicionário: {meu_dicionario}")

meu_dicionario['c'] = 'Catapimbas' # Atualizando valor da chave 'c'
meu_dicionario['f'] = 'Ramon S2' # Adicionando nova chave 'f'
print(f"Dicionário atualizado: {meu_dicionario}")